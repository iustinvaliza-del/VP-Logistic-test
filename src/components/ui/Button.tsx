import { cn } from "@/lib/utils";
import { forwardRef } from "react";

interface ButtonProps extends React.ButtonHTMLAttributes<HTMLButtonElement> {
  variant?: "primary" | "secondary" | "outline" | "ghost";
  size?: "sm" | "md" | "lg";
  asChild?: boolean;
}

const Button = forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant = "primary", size = "md", children, ...props }, ref) => {
    return (
      <button
        ref={ref}
        className={cn(
          "inline-flex items-center justify-center font-medium transition-all duration-200 rounded-lg cursor-pointer",
          {
            "bg-brand-accent text-white hover:bg-brand-accent-dark shadow-sm hover:shadow-md":
              variant === "primary",
            "bg-brand-primary text-white hover:bg-brand-primary/90":
              variant === "secondary",
            "border-2 border-brand-accent text-brand-accent hover:bg-brand-accent hover:text-white":
              variant === "outline",
            "text-brand-primary hover:bg-brand-light":
              variant === "ghost",
          },
          {
            "px-4 py-2 text-sm": size === "sm",
            "px-6 py-3 text-base": size === "md",
            "px-8 py-4 text-lg": size === "lg",
          },
          className
        )}
        {...props}
      >
        {children}
      </button>
    );
  }
);

Button.displayName = "Button";

export { Button };
